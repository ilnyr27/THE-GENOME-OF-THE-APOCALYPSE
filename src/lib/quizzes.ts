export function getQuiz(bookId: number, chapterId: number): Quiz | null {
  return quizzes[`${bookId}-${chapterId}`] || null;
}

export function getQuizChapters(bookId: number): number[] {
  return Object.keys(quizzes)
    .filter((k) => k.startsWith(`${bookId}-`))
    .map((k) => parseInt(k.split("-")[1]))
    .sort((a, b) => a - b);
}
